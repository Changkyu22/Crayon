package com.nuri.s5.model;

public class TourNoticeVO {
	private int num;
	private String compared;
	private String include;
	private String exclude;
	private String alert;
	private String prepared;
	private String attetion;
	private String refund;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getCompared() {
		return compared;
	}
	public void setCompared(String compared) {
		this.compared = compared;
	}
	public String getInclude() {
		return include;
	}
	public void setInclude(String include) {
		this.include = include;
	}
	public String getExclude() {
		return exclude;
	}
	public void setExclude(String exclude) {
		this.exclude = exclude;
	}
	public String getAlert() {
		return alert;
	}
	public void setAlert(String alert) {
		this.alert = alert;
	}
	public String getPrepared() {
		return prepared;
	}
	public void setPrepared(String prepared) {
		this.prepared = prepared;
	}
	public String getAttetion() {
		return attetion;
	}
	public void setAttetion(String attetion) {
		this.attetion = attetion;
	}
	public String getRefund() {
		return refund;
	}
	public void setRefund(String refund) {
		this.refund = refund;
	}
	
	
	
}
